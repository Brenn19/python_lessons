# ============================================
# Задача 1. Калькулятор целых чисел (int)
# ============================================
print("=== Задача 1. Калькулятор целых чисел ===")
# Ввод двух целых чисел
a = int(input("Введите первое целое число: "))
b = int(input("Введите второе целое число: "))

# Вычисление и вывод результатов
print(f"Сумма: {a + b}")
print(f"Разность: {a - b}")
print(f"Произведение: {a * b}")
print(f"Целочисленное деление: {a // b}")
print(f"Остаток от деления: {a % b}")
print(f"Степень: {a ** b}")
print("\n" + "="*40 + "\n")


# ============================================
# Задача 2. Площадь круга и длина окружности (float)
# ============================================
print("=== Задача 2. Площадь круга и длина окружности ===")
# Ввод радиуса (вещественное число)
radius = float(input("Введите радиус окружности: "))
pi = 3.14159

area = pi * radius ** 2
circumference = 2 * pi * radius

# Округление до двух знаков
print(f"Площадь круга: {area:.2f}")
print(f"Длина окружности: {circumference:.2f}")
print("\n" + "="*40 + "\n")


# ============================================
# Задача 3. Анализ строки (str)
# ============================================
print("=== Задача 3. Анализ строки ===")
# Ввод произвольной строки
text = input("Введите произвольную строку: ")

# Вывод информации о строке
print(f"Длина строки: {len(text)}")
print(f"Первый символ: {text[0] if text else 'строка пуста'}")
print(f"Последний символ: {text[-1] if text else 'строка пуста'}")
print(f"В верхнем регистре: {text.upper()}")
print(f"Количество пробелов: {text.count(' ')}")
print("\n" + "="*40 + "\n")
